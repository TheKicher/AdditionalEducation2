package com.hfad.aded;

public class User {

    public User(String id, String city, String name, String prof, String disc, String price, String phone, String email) {
        this.id = id;
        this.city = city;
        this.name = name;
        this.prof = prof;
        this.disc = disc;
        this.price = price;
        this.phone = phone;
        this.email = email;
    }

    public String id;
    public String city;
    public String name;
    public String prof;
    public String disc;
    public String price;
    public String phone;
    public String email;


    public User() {
    }


}

